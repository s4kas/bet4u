package com.bmp.bet4u.client.estatisticas.beans;

public class ResultadoJornada {

	private String epoca;
	private Integer jornada;
	private Integer equipaCasa;
	private Integer equipaFora;
	private Integer golosCasa;
	private Integer golosFora;
	
	public String getEpoca() {
		return epoca;
	}
	public void setEpoca(String epoca) {
		this.epoca = epoca;
	}
	public Integer getJornada() {
		return jornada;
	}
	public void setJornada(Integer jornada) {
		this.jornada = jornada;
	}
	public Integer getEquipaCasa() {
		return equipaCasa;
	}
	public void setEquipaCasa(Integer equipaCasa) {
		this.equipaCasa = equipaCasa;
	}
	public Integer getEquipaFora() {
		return equipaFora;
	}
	public void setEquipaFora(Integer equipaFora) {
		this.equipaFora = equipaFora;
	}
	public Integer getGolosCasa() {
		return golosCasa;
	}
	public void setGolosCasa(Integer golosCasa) {
		this.golosCasa = golosCasa;
	}
	public Integer getGolosFora() {
		return golosFora;
	}
	public void setGolosFora(Integer golosFora) {
		this.golosFora = golosFora;
	}
}
